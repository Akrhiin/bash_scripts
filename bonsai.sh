#!/bin/sh

# called as:
#   bash ~/Documents/Programming/bash_scripts/bonsai.sh [pot: 0,1] [seed: 1-20] 

# Set Seeds
# if no arguments
if [ $# -eq 0 ]
then
    pot_seed=0
    seed=$[ $RANDOM % 3 + 1]
else
    # if first argument
    if [ ! -z "$1" ]
    then
        pot_seed=$1
    # if second argument
        if [ ! -z "$2" ]
        then
            seed=$2
        else
            seed=$[ $RANDOM % 3 + 1]
        fi
    fi
fi

# Set Pot
# decorated pot -> 0
if [ "$pot_seed" -eq 0 ]
then
    pot="============./~~\\.============
 \\  /\\  /\\  /\\  /\\  /\\  /\\  / 
  \\/__\\/__\\/__\\/__\\/__\\/__\\/ 
  (_)                    (_)"
# undecorated pot -> 1
elif [ "$pot_seed" -eq 1 ]
then
    pot="============./~~\\.============
 \\                          /
  \\________________________/
  (_)                    (_)"
fi

# Set Tree
case $seed in
    1)
        tree="          &
         & &&&
          &&&&&   && &
           &/|&  & &&&&&& &
        &  &/~&/~ &&&_/&&& &&
            /~/      _/      &
            /| _/  \|_/
            \\   _/
  &       & &\\
  &&&& &   &  /|
& &&& &&& &   /
 &&&&& /\\_ /  \\_
           / /
            /~/|
             /
            /~/~
              /~/|
               /|"
        ;;
    2)
        tree="             &&
           & & &&
             &&&&
               /~&&&&&
               /|\\ &&&&
        &      /~\\ &\\& &&
        &&&    /|\\ &&&&   &
      & \\&&&&&&&\\| &|/&&
        &&&&&&&&&&\\~\\|
&   & &&&&&&&&&&&&&  ~\\/
 &  &&  &&&&|&&&~\\|  |/
     \\&&&&& &\\&&&&&/|\\&&
     &&&&&&_\\~/&&&&&&&& &&
     &&   &\\\\_\_/&\&&/&&&&&
      &&    \\|~\\\\/~\\  && &
             |/~\\|                 &       &  &  &
           /|\\   ~\\|                &   &&|&&
            \\|  \\|~~\\/|_/    _/      &&&&&&&&&
       \\\\___|/~\\|/~\\ __//_/_/_//_/   &/ & &
 _\\ && \\__  \\  ~\\|                /_/    &
 &\\&__\\     |\\  |~\\
            \\_~\\/
              ~\\\\"
        ;;
    3)
        tree="                   ~ ~&
                  ~  ~& &
                     ~&  ~&
                  ~&/ ~ ~&
                    ~&    ~&
                   ~& /
                   //~\\
                    /|\\
      ~&            /~|___/
     ~& &             /|\\ /
       ~&  ~&         /| ~& &
       ~& & &  ~&    |/~  ~  ~&
      \\_  ~&        /~|/    ~ ~&
         ~&     \\_  \\/~ ~& ~& ~&
           \\____\\__/|/~ ~& ~&
                //~          ~&
              /~/~"
        ;;
esac

# Output
echo ""
echo "$tree"
echo "$pot"
echo ""
echo "Bonsai v0.1"
echo ""
